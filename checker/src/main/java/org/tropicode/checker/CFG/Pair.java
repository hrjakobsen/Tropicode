package org.tropicode.checker.CFG;

public class Pair<L, R> {
    private final L left;
    private final R right;

    public Pair(L left, R right) {
        assert left != null;
        assert right != null;

        this.left = left;
        this.right = right;
    }

    public L getLeft() {
        return left;
    }

    public R getRight() {
        return right;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        Pair<?, ?> pair = (Pair<?, ?>) o;

        if (!left.equals(pair.left)) {
            return false;
        }
        return right.equals(pair.right);
    }

    @Override
    public String toString() {
        return "<" + left + ", " + right + ">";
    }
}
